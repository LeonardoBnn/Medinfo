    <h1>Politique de Confidentialité</h1>

    <p>La plateforme MedInfo respecte le Règlement Général sur la Protection des Données (RGPD) et protège les données personnelles et médicales des utilisateurs.</p>

    <h2>1. Données collectées</h2>
    <p>Nom, prénom, date de naissance, adresse, téléphone, email, informations médicales nécessaires à la prise de rendez-vous.</p>

    <h2>2. Finalités</h2>
    <p>Gestion des rendez-vous, suivi médical, communication entre patients et médecins.</p>

    <h2>3. Durée de conservation</h2>
    <p>Les données sont conservées 3 ans après le dernier contact, conformément au registre des traitements.</p>

    <h2>4. Droits des utilisateurs</h2>
    <p>Droit d’accès, de rectification, d’effacement, d’opposition, de portabilité. Pour exercer vos droits : contact@ramsay-saintlazare.fr</p>

    <h2>5. Sécurité</h2>
    <p>Les données sont stockées sur un serveur sécurisé. Les mots de passe sont chiffrés et les accès sont limités au personnel habilité.</p>
